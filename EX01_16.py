import turtle

turtle.color("black")
turtle.penup()
turtle.goto(50, 60)
turtle.pendown()
turtle.circle(50)

turtle.color("black")
turtle.penup()
turtle.goto(-50, 60)
turtle.pendown()
turtle.circle(50)

turtle.color("black")
turtle.penup()
turtle.goto(-50, -40)
turtle.pendown()
turtle.circle(50)

turtle.color("black")
turtle.penup()
turtle.goto(50, -40)
turtle.pendown()
turtle.circle(50)
turtle.done()
