import turtle

turtle.showturtle()


#Two Triangle

turtle.penup()
turtle.goto(-40, 80)
turtle.pendown()

turtle.right(120)
turtle.forward(100)

turtle.penup()
turtle.goto(-40, -8)
turtle.pendown()
turtle.right(120)
turtle.forward(100)

turtle.penup()
turtle.goto(-40, 80)
turtle.pendown()

turtle.right(300)
turtle.forward(51)

turtle.penup()
turtle.goto(-40, -8)
turtle.pendown()
turtle.right(360)
turtle.forward(50)

turtle.done()
